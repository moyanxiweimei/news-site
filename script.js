// ===== Filter News =====
function filterNews(query, category) {
  let hasResults = false;
  
  Object.keys(newsData).forEach(key => {
    const section = document.getElementById(key);
    if (!section) return;
    
    if (category !== 'all' && category !== key) {
      section.classList.add('hidden');
      return;
    }
    section.classList.remove('hidden');
    
    const cards = section.querySelectorAll('.news-card');
    let sectionHasResults = false;
    
    cards.forEach(card => {
      const title = card.querySelector('h3').textContent.toLowerCase();
      const summary = card.querySelector('p').textContent.toLowerCase();
      const matches = title.includes(query) || summary.includes(query);
      
      if (matches) {
        card.classList.remove('hidden');
        sectionHasResults = true;
        hasResults = true;
      } else {
        card.classList.add('hidden');
      }
    });
    
    // Update section count
    const countEl = section.querySelector('.section-count');
    if (countEl) {
      const visibleCards = section.querySelectorAll('.news-card:not(.hidden)');
      countEl.textContent = visibleCards.length + ' 条';
    }
  });
  
  // Show no results message
  let noResults = document.querySelector('.no-results');
  if (!hasResults) {
    if (!noResults) {
      noResults = document.createElement('div');
      noResults.className = 'no-results';
      noResults.innerHTML = `
        <div class="no-results-icon">🔍</div>
        <h3>未找到相关资讯</h3>
        <p>尝试其他关键词或切换分类</p>
      `;
      document.querySelector('main').appendChild(noResults);
    }
    noResults.style.display = 'block';
  } else if (noResults) {
    noResults.style.display = 'none';
  }
}

// ===== Image Loading =====
function initImageLoading() {
  document.querySelectorAll('.card-image').forEach(img => {
    if (img.complete) {
      img.classList.add('loaded');
      const skeleton = img.parentElement.querySelector('.skeleton-bg');
      if (skeleton) skeleton.remove();
    } else {
      img.addEventListener('load', () => {
        img.classList.add('loaded');
        const skeleton = img.parentElement.querySelector('.skeleton-bg');
        if (skeleton) skeleton.remove();
      });
      img.addEventListener('error', () => {
        const skeleton = img.parentElement.querySelector('.skeleton-bg');
        if (skeleton) skeleton.remove();
        img.style.display = 'none';
      });
    }
  });
}

// ===== Modal =====
const modalOverlay = document.getElementById('modalOverlay');
const modalClose = document.getElementById('modalClose');
const modalImg = document.getElementById('modalImg');
const modalTitle = document.getElementById('modalTitle');
const modalMeta = document.getElementById('modalMeta');
const modalContent = document.getElementById('modalContent');
const modalBadge = document.getElementById('modalBadge');
const modalLink = document.getElementById('modalLink');

let currentModalData = null;

function openModal(data, imgSrc) {
  currentModalData = data;
  modalImg.src = imgSrc || '';
  modalImg.style.display = imgSrc ? 'block' : 'none';
  modalTitle.textContent = data.title;
  modalMeta.textContent = `${data.source} · ${data.date}`;
  
  // Format detail text
  let content = data.detail || data.summary;
  content = content.replace(/\\n\\n/g, '</p><p>').replace(/\\n/g, ' ');
  if (!content.startsWith('<p>')) content = '<p>' + content + '</p>';
  modalContent.innerHTML = content;
  
  if (data.badge) {
    modalBadge.textContent = data.badge;
    modalBadge.style.display = 'inline-block';
  } else {
    modalBadge.style.display = 'none';
  }
  
  if (data.link) {
    modalLink.href = data.link;
    modalLink.style.display = 'inline-flex';
  } else {
    modalLink.style.display = 'none';
  }
  
  modalOverlay.classList.add('active');
  document.body.style.overflow = 'hidden';
}

function closeModal() {
  modalOverlay.classList.remove('active');
  document.body.style.overflow = '';
  currentModalData = null;
}

if (modalClose) modalClose.addEventListener('click', closeModal);
if (modalOverlay) {
  modalOverlay.addEventListener('click', (e) => {
    if (e.target === modalOverlay) closeModal();
  });
}
document.addEventListener('keydown', (e) => {
  if (e.key === 'Escape') closeModal();
});

// ===== Card Click =====
document.querySelectorAll('.news-card').forEach(card => {
  card.addEventListener('click', (e) => {
    if (e.target.closest('.card-action-btn')) return;
    
    const gridId = card.dataset.grid;
    const index = parseInt(card.dataset.index);
    const sectionKey = gridId.replace('-grid', '');
    const data = newsData[sectionKey][index];
    const img = card.querySelector('.card-image');
    const imgSrc = img && img.src ? img.src : '';
    openModal(data, imgSrc);
  });
});

// ===== Favorite =====
function toggleFavorite(e, cardId) {
  e.stopPropagation();
  const btn = e.currentTarget;
  const index = favorites.indexOf(cardId);
  
  if (index > -1) {
    favorites.splice(index, 1);
    btn.classList.remove('active');
    btn.textContent = '🤍';
    showToast('已取消收藏');
  } else {
    favorites.push(cardId);
    btn.classList.add('active');
    btn.textContent = '❤️';
    showToast('已收藏');
  }
  
  localStorage.setItem('newsFavorites', JSON.stringify(favorites));
}

// ===== Share =====
async function shareNews(e, title, summary) {
  e.stopPropagation();
  
  if (navigator.share) {
    try {
      await navigator.share({
        title: title,
        text: summary,
        url: window.location.href
      });
    } catch (err) {
      // User cancelled
    }
  } else {
    // Copy to clipboard
    const text = `${title}\n${summary}\n${window.location.href}`;
    navigator.clipboard.writeText(text).then(() => {
      showToast('链接已复制到剪贴板');
    });
  }
}

function shareModalNews() {
  if (currentModalData) {
    shareNews({ stopPropagation: () => {} }, currentModalData.title, currentModalData.summary);
  }
}

// ===== Toast =====
function showToast(message) {
  let toast = document.querySelector('.toast');
  if (!toast) {
    toast = document.createElement('div');
    toast.className = 'toast';
    document.body.appendChild(toast);
  }
  toast.textContent = message;
  toast.classList.add('show');
  setTimeout(() => toast.classList.remove('show'), 2000);
}

